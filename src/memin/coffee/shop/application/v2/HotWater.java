package memin.coffee.shop.application.v2;

public class HotWater {

    private int id = 7;
    private String name = "How Water";
    private int price = 3;
    private int hotWaterAmount = 1;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getHotWaterAmount() {
        return hotWaterAmount;
    }

    public void setHotWaterAmount(int hotWaterAmount) {
        this.hotWaterAmount = hotWaterAmount;
    }
}
